def simulate_session(bars_raw, daily_ohlcv, lookup, symbol, td, expiry_idx, capital):
    lot_size = LOT_SIZE[symbol]
    ed = nearest_expiry_db(td, expiry_idx)
    if ed is None:
        return []
    bars = [Bar(
        bar_ts=r["bar_ts"] if isinstance(r["bar_ts"], datetime) else datetime.fromisoformat(r["bar_ts"]),
        open=float(r["open"]), high=float(r["high"]),
        low=float(r["low"]), close=float(r["close"]),
        trade_date=td,
    ) for r in bars_raw]
    if len(bars) < 30:
        return []
    dates_sorted = sorted(daily_ohlcv.keys())
    prior_dates = [d for d in dates_sorted if d < str(td)]
    prior_high = prior_low = None
    if prior_dates:
        pd = daily_ohlcv[prior_dates[-1]]
        prior_high, prior_low = pd["high"], pd["low"]
    results = []
    tss = [b.bar_ts for b in bars]
    seen_bar_ts = set()
    detector = ICTDetector(symbol=symbol)
    for pat_idx in range(10, len(bars)):
        bar = bars[pat_idx]
        if bar.bar_ts.time() >= POWER_HOUR:
            break
        htf_zones = build_simulated_htf_zones(daily_ohlcv, bars[:pat_idx], symbol, td)
        start = max(0, pat_idx - 10)
        window_bars = bars[start:pat_idx + 1]
        patterns = detector.detect(
            bars=window_bars, atm_iv=None,
            htf_zones=htf_zones, prior_high=prior_high, prior_low=prior_low,
        )
        for pattern in patterns:
            key = (pattern.pattern_type, pattern.bar_ts)
            if key in seen_bar_ts:
                continue
            if pattern.bar_ts.time() >= POWER_HOUR:
                continue
            if pattern.ict_tier == "SKIP":
                continue
            seen_bar_ts.add(key)
            htf_tf = "NONE"
            if pattern.htf_zone_id:
                for z in htf_zones:
                    if z.id == pattern.htf_zone_id:
                        htf_tf = z.timeframe
                        break
            tier_mult = TIER_MULT.get(pattern.ict_tier, 1.0)
            base_lots = max(1, int(capital / 100_000))
            lots = max(1, int(base_lots * tier_mult))
            entry_strike = atm_strike(pattern.spot_at_detection, symbol)
            entry_price = get_option_price_at(lookup, entry_strike, pattern.opt_type, pattern.bar_ts, symbol)
            if entry_price is None:
                continue
            capital_deployed = entry_price * lots * lot_size
            try:
                entry_idx = tss.index(pattern.bar_ts)
            except ValueError:
                entry_idx = bisect.bisect_left(tss, pattern.bar_ts)
                if entry_idx >= len(bars):
                    continue
            t30_ts = pattern.bar_ts + timedelta(minutes=30)
            t30_price = get_option_price_at(lookup, entry_strike, pattern.opt_type, t30_ts, symbol)
            pnl_t30 = None
            if t30_price:
                pnl_t30 = (t30_price - entry_price) * lots * lot_size
            ict_ts, ict_price, ict_reason = find_structure_break_exit(
                bars, entry_idx, pattern.direction,
                pattern.spot_at_detection, pattern.zone_low, pattern.zone_high,
                lookup, entry_strike, pattern.opt_type, symbol
            )
            pnl_ict = None
            if ict_price:
                pnl_ict = (ict_price - entry_price) * lots * lot_size
            results.append(TradeResult(
                td=td, symbol=symbol, pattern_type=pattern.pattern_type,
                direction=pattern.direction, ot=pattern.opt_type,
                ict_tier=pattern.ict_tier, mtf_context=pattern.mtf_context,
                htf_timeframe=htf_tf, time_zone=pattern.time_zone,
                entry_ts=pattern.bar_ts, entry_price=entry_price,
                strike=entry_strike, entry_spot=pattern.spot_at_detection,
                zone_high=pattern.zone_high, zone_low=pattern.zone_low,
                capital_deployed=capital_deployed, lots=lots,
                exit_t30_ts=t30_ts, exit_t30_price=t30_price, pnl_t30=pnl_t30,
                exit_ictexit_ts=ict_ts, exit_ictexit_price=ict_price,
                exit_ictexit_reason=ict_reason, pnl_ict=pnl_ict,
                mom_aligned=pattern.mom_aligned,
                impulse_strong=pattern.impulse_strong,
                has_prior_sweep=pattern.has_prior_sweep,
            ))
    return results


def main():
    sb = create_client(SUPABASE_URL, SUPABASE_KEY)
    log("Fetching instrument IDs...")
    inst = {r["symbol"]: r["id"] for r in
            sb.table("instruments").select("id, symbol").execute().data}

    all_trades = []
    capital_state = {"NIFTY": STARTING_CAPITAL, "SENSEX": STARTING_CAPITAL}
    session_log = []

    for symbol in ["NIFTY", "SENSEX"]:
        log(f"\n-- {symbol} --")
        log("  Building expiry index...")
        expiry_idx = build_expiry_index_simple(sb, inst[symbol])
        log(f"  {len(expiry_idx)} weekly expiries found")
        log("  Loading spot bars...")
        raw = fetch_paginated(
            sb, "hist_spot_bars_1m",
            [("eq","instrument_id",inst[symbol]),("eq","is_pre_market",False)],
            "bar_ts, trade_date, open, high, low, close"
        )
        for r in raw:
            r["bar_ts"] = datetime.fromisoformat(r["bar_ts"])
            r["trade_date"] = date.fromisoformat(r["trade_date"])
            for k in ("open","high","low","close"):
                r[k] = float(r[k])
        spot_sessions = sessions_from_bars(raw)
        dates = sorted(spot_sessions.keys())
        log(f"  {len(raw):,} bars | {len(dates)} sessions")

        daily_ohlcv = {}
        for td_str, bars in spot_sessions.items():
            daily_ohlcv[str(td_str)] = {
                "date": td_str,
                "open": float(bars[0]["open"]),
                "high": max(float(b["high"]) for b in bars),
                "low": min(float(b["low"]) for b in bars),
                "close": float(bars[-1]["close"]),
            }

        capital = capital_state[symbol]
        scored = skipped = 0

        for i, td in enumerate(dates):
            bars_raw = spot_sessions[td]
            ed = nearest_expiry_db(td, expiry_idx)
            if ed is None:
                skipped += 1
                continue
            open_spot = float(bars_raw[0]["open"])
            step = STRIKE_STEP[symbol]
            base_atm = atm_strike(open_spot, symbol)
            strikes = {base_atm + off * step for off in range(-10, 11)}
            try:
                lookup = fetch_option_day(sb, inst[symbol], td, ed, sorted(strikes), ["CE","PE"])
            except Exception:
                skipped += 1
                continue
            if not lookup:
                skipped += 1
                continue
            prior_day_count = sum(1 for d in daily_ohlcv if str(d) < str(td))
            if prior_day_count < 5:
                skipped += 1
                continue
            trades = simulate_session(bars_raw, daily_ohlcv, lookup, symbol, td, expiry_idx, capital)
            session_pnl_t30 = sum(t.pnl_t30 for t in trades if t.pnl_t30 is not None)
            n = len(trades)
            cap_start = capital
            if session_pnl_t30 > 0:
                capital += session_pnl_t30
            else:
                capital += session_pnl_t30
            session_log.append((td, symbol, n, session_pnl_t30, cap_start, capital))
            all_trades.extend(trades)
            scored += 1
            if i % 20 == 0:
                log(f"    {i}/{len(dates)} | scored={scored} | "
                    f"capital=Rs {capital:,.0f} | trades_so_far={len(all_trades)}")

        capital_state[symbol] = capital
        log(f"  Complete -- {scored} sessions | {len([t for t in all_trades if t.symbol==symbol])} trades | "
            f"Final capital Rs {capital:,.0f}")

    # --- CSV DUMP (the only thing this script adds vs original) ---
    stamp = datetime.now().strftime("%Y%m%d_%H%M")
    csv_path = f"exp15_trades_{stamp}.csv"
    fieldnames = [f.name for f in TradeResult.__dataclass_fields__.values()]
    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for t in all_trades:
            row = asdict(t)
            for k, v in row.items():
                if isinstance(v, (datetime, date)):
                    row[k] = v.isoformat()
            w.writerow(row)
    log(f"Trade list dumped: {csv_path}  ({len(all_trades)} rows)")

    # Also dump session_log
    session_csv = f"exp15_sessions_{stamp}.csv"
    with open(session_csv, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["trade_date", "symbol", "n_trades", "session_pnl_t30",
                    "capital_start", "capital_end"])
        for row in session_log:
            td, sym, n, pnl, cs, ce = row
            w.writerow([str(td), sym, n, pnl, cs, ce])
    log(f"Session log dumped: {session_csv}")

    # Summary
    nifty_trades = [t for t in all_trades if t.symbol == "NIFTY"]
    sensex_trades = [t for t in all_trades if t.symbol == "SENSEX"]
    log(f"")
    log(f"Final NIFTY  capital: Rs {capital_state['NIFTY']:,.0f}  "
        f"({100*(capital_state['NIFTY']-STARTING_CAPITAL)/STARTING_CAPITAL:+.1f}%)  "
        f"N_trades={len(nifty_trades)}")
    log(f"Final SENSEX capital: Rs {capital_state['SENSEX']:,.0f}  "
        f"({100*(capital_state['SENSEX']-STARTING_CAPITAL)/STARTING_CAPITAL:+.1f}%)  "
        f"N_trades={len(sensex_trades)}")
    log(f"")
    log(f"Next step: run analyze_exp15_trades.py {csv_path}")


if __name__ == "__main__":
    main()
